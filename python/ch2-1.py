# 2.1 引言

# 有效使用内置数据类型和用户定义的数据类型
# 是数据处理型应用的基础。

# 2.1.1 原始数据类型

type(2) # <class 'int'>

12 + 3000000000000000000000000 # 3000000000000000000000012

# Python 包含三种原始数字类型：
# 整数（int）、浮点数（float）和复数（complex）

type(1.5) # <class 'float'>
type(1+1j) # <class 'complex'>

7 / 3 * 3 # 7.0
1 / 3 * 7 * 3 # 6.999999999999999

type(1/3) # <class 'float'>
1/3 # 0.3333333333333333

1/3 == 0.333333333333333312345  # True